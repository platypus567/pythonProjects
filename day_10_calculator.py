def add(n1, n2):
  return n1 + n2

def subtract(n1, n2):
  return n1 - n2

def multiply(n1, n2):
  return n1 * n2

def divide(n1, n2):
  return n1 / n2
operations = {"+": add, 
"-": subtract,
"*": multiply, 
"/": divide}

def calculator():
  print(logo)
  should_continue = True
  num1 = float(input("What's the first number?\n"))
  for key in operations:
    print(key)
  while should_continue == True:

    operation = input("What operation are you using?\n")
    num2 = float(input("What's the next number?\n"))
    calculation_function = operations[operation]
    answer = calculation_function(num1,num2)
    print(f"{num1} {operation} {num2} = {answer}")
    if input(f"Type y to continue calculating with {answer}, type n to start a new calculation\n") == "y":
      num1 = answer
      
    else:
      should_continue = False
      print(chr(27) + "[2J")
      calculator()
calculator()



















